// fakeJournal builds a tiny, fully-typed envelope slice for the scaffolder.
// Two turns: first uses a tool, second is a final response. Token counts
// follow the build-error-fix shape so observe() pulls realistic totals.
func fakeJournal() []journal.Envelope {
	t0 := time.Date(2026, 5, 1, 12, 0, 0, 0, time.UTC)
	step := func(d time.Duration) time.Time { t0 = t0.Add(d); return t0 }
	return []journal.Envelope{
		{Seq: 1, Ts: step(0), Type: "io.session.start", Payload: map[string]any{"session_id": "fake"}},
		{Seq: 2, Ts: step(10 * time.Millisecond), Type: "io.input", Payload: map[string]any{"Content": "Why won't main.go build?"}},
		{Seq: 3, Ts: step(10 * time.Millisecond), Type: "agent.turn.start"},
		{Seq: 4, Ts: step(10 * time.Millisecond), Type: "llm.response", Payload: map[string]any{
			"Usage": map[string]any{"PromptTokens": 80, "CompletionTokens": 40},
		}},
		{Seq: 5, Ts: step(10 * time.Millisecond), Type: "tool.invoke", Payload: map[string]any{
			"Name": "read_file", "Arguments": map[string]any{"path": "main.go"},
		}},
		{Seq: 6, Ts: step(10 * time.Millisecond), Type: "tool.result", Payload: map[string]any{}},
		{Seq: 7, Ts: step(10 * time.Millisecond), Type: "llm.response", Payload: map[string]any{
			"Usage": map[string]any{"PromptTokens": 140, "CompletionTokens": 70},
		}},
		{Seq: 8, Ts: step(10 * time.Millisecond), Type: "agent.turn.end"},
	}
}

// TestObserve_CountsAndTotals confirms the per-pass projection captures
// event counts, token totals, and per-tool stats faithfully.
func TestObserve_CountsAndTotals(t *testing.T) {
	o := observe(fakeJournal())
	if got := o.EventCounts["io.input"]; got != 1 {
		t.Errorf("io.input count = %d want 1", got)
	}
	if got := o.EventCounts["llm.response"]; got != 2 {
		t.Errorf("llm.response count = %d want 2", got)
	}
	if got := o.PromptTokens; got != 220 {
		t.Errorf("PromptTokens = %d want 220", got)
	}
	if got := o.CompletionTokens; got != 110 {
		t.Errorf("CompletionTokens = %d want 110", got)
	}
	if got := o.ToolCounts["read_file"]; got != 1 {
		t.Errorf("read_file count = %d want 1", got)
	}
	if _, ok := o.ToolArgKeys["read_file"]["path"]; !ok {
		t.Errorf("read_file arg keys missing 'path': %v", o.ToolArgKeys["read_file"])
	}
	// Two turn-start (1) and turn-end (1) events; the journal closes a single
	// turn so we expect one duration sample.
	if len(o.TurnDurationsMs) != 1 {
		t.Errorf("TurnDurationsMs len = %d want 1; got %v", len(o.TurnDurationsMs), o.TurnDurationsMs)
	}
}

// TestObserve_Idempotent runs observe twice on the same input and checks
// the snapshots match. Pure functions should not drift.
func TestObserve_Idempotent(t *testing.T) {
	envs := fakeJournal()
	a := observe(envs)
	b := observe(envs)
	if !reflect.DeepEqual(a.EventCounts, b.EventCounts) {
		t.Errorf("idempotency: EventCounts drift")
	}
	if !reflect.DeepEqual(a.ToolCounts, b.ToolCounts) {
		t.Errorf("idempotency: ToolCounts drift")
	}
	if a.PromptTokens != b.PromptTokens {
		t.Errorf("idempotency: PromptTokens drift")
	}
}

// TestObserve_NonReplayableHits checks the warn-list lights up for known
// non-replayable types and stays empty otherwise.
func TestObserve_NonReplayableHits(t *testing.T) {
	envs := []journal.Envelope{
		{Seq: 1, Type: "provider.fallback.error", Payload: map[string]any{}},
	}
	o := observe(envs)
	if len(o.NonReplayableHits) != 1 || o.NonReplayableHits[0] != "provider.fallback.error" {
		t.Errorf("NonReplayableHits = %v want [provider.fallback.error]", o.NonReplayableHits)
	}

	clean := observe(fakeJournal())
	if len(clean.NonReplayableHits) != 0 {
		t.Errorf("clean journal flagged %v as non-replayable", clean.NonReplayableHits)
	}
}

// TestSynthesizeAssertions_Parses round-trips the rendered YAML through
// the case loader's parser to confirm the schema matches what evalcase
// expects. This is the contract between Promote and the runner.
func TestSynthesizeAssertions_Parses(t *testing.T) {
	data, err := SynthesizeAssertions(fakeJournal())
	if err != nil {
		t.Fatalf("SynthesizeAssertions: %v", err)
	}

	// Sanity: comments survive.
	if !bytes.Contains(data, []byte("Auto-generated by `nexus eval promote`")) {
		t.Errorf("synth output missing top-of-file comment:\n%s", data)
	}
	if !bytes.Contains(data, []byte("kind: event_count_bounds")) {
		t.Errorf("synth output missing event_count_bounds entry:\n%s", data)
	}
	if !bytes.Contains(data, []byte("kind: token_budget")) {
		t.Errorf("synth output missing token_budget entry:\n%s", data)
	}
	if !bytes.Contains(data, []byte("kind: latency")) {
		t.Errorf("synth output missing latency entry:\n%s", data)
	}
	if !bytes.Contains(data, []byte("kind: tool_invocation_parity")) {
		t.Errorf("synth output missing tool_invocation_parity entry:\n%s", data)
	}
	if !bytes.Contains(data, []byte("TODO: add an llm_judge rubric")) {
		t.Errorf("synth output missing semantic-block TODO:\n%s", data)
	}
}

// TestSynthesizeAssertions_Idempotent re-renders against the same envelopes
// and asserts byte-stability — production needs a stable diff for review.
func TestSynthesizeAssertions_Idempotent(t *testing.T) {
	envs := fakeJournal()
	a, err := SynthesizeAssertions(envs)
	if err != nil {
		t.Fatalf("first synth: %v", err)
	}
	b, err := SynthesizeAssertions(envs)
	if err != nil {
		t.Fatalf("second synth: %v", err)
	}
	if !bytes.Equal(a, b) {
		t.Errorf("synth output drifted between runs:\n--- first ---\n%s\n--- second ---\n%s", a, b)
	}
}

// TestSynthesizeAssertions_NoTools ensures the tool_invocation_parity block
// is omitted when no tool.invoke events are present (otherwise the block
// would be a no-op tracking nothing).
func TestSynthesizeAssertions_NoTools(t *testing.T) {
	envs := []journal.Envelope{
		{Seq: 1, Ts: time.Now(), Type: "io.input", Payload: map[string]any{"Content": "hi"}},
		{Seq: 2, Ts: time.Now(), Type: "agent.turn.start"},
		{Seq: 3, Ts: time.Now(), Type: "llm.response", Payload: map[string]any{}},
		{Seq: 4, Ts: time.Now(), Type: "agent.turn.end"},
	}
	data, err := SynthesizeAssertions(envs)
	if err != nil {
		t.Fatalf("SynthesizeAssertions: %v", err)
	}
	if strings.Contains(string(data), "kind: tool_invocation_parity") {
		t.Errorf("expected no tool_invocation_parity block when no tools observed:\n%s", data)
	}
}

// TestPercentileAndSlack covers the small numeric helpers the scaffold
// uses for budget construction. Boundary cases first: empty input, single
// element, zero-with-nonzero-pct.
func TestPercentileAndSlack(t *testing.T) {
	if got := percentile(nil, 0.5); got != 0 {
		t.Errorf("percentile(nil, 0.5) = %d want 0", got)
	}
	if got := percentile([]int{42}, 0.95); got != 42 {
		t.Errorf("percentile([42], 0.95) = %d want 42", got)
	}
	if got := percentile([]int{1, 2, 3, 4, 5}, 0.5); got != 3 {
		t.Errorf("percentile([1..5], 0.5) = %d want 3", got)
	}

	if got := withSlack(0, 0.10); got != 0 {
		t.Errorf("withSlack(0, 0.10) = %d want 0", got)
	}
	if got := withSlack(100, 0.10); got != 110 {
		t.Errorf("withSlack(100, 0.10) = %d want 110", got)
	}
	// Tiny base values still gain a +1 slack so the bound is strictly above
	// the observed value.
	if got := withSlack(5, 0.10); got <= 5 {
		t.Errorf("withSlack(5, 0.10) = %d want > 5", got)
	}
}
